# ali is an id for bool class
ali = bool()
# print type of ali
print("type of ali = ", end='')
print(type(ali))
# print value of ali
print("value of ali =", end='')
print(ali)

# ali is id for True, in the bool class
ali = True
# print type of ali
print("type of ali = ", end='')
print(type(ali))
# print value of ali
print("value of ali =", end='')
print(ali)

# ali_2 is ID for True
ali_2 = True
# print bool value of ali_2
print(bool(ali_2))

# ali_3 is ID for string
ali_3 = "sdfsadf"
print(ali_3)
# print type of ali_3
print("type of ali_3 = ", end='')
print(type(ali_3))
# print bool value of ali_3
print(bool(ali_3))

